import Foundation

struct Jet: ColorMap {
    let sections = 8
    let length: Int
    var colorMapInSIMD4: [SIMD4<Float>]
    
    init(_ length: Int) {
        self.length = length
        self.colorMapInSIMD4 = [SIMD4<Float>]()
        
        for k in 0..<length {
            colorMapInSIMD4.append(colorInSIMD4(k)!)
        }
    }
    
    func colorInSIMD4(_ index: Int) -> SIMD4<Float>? {
        guard (index >= 0 && index < length) else {
            return nil
        }
        
        let section = sections * index / length
        let sectionWidth = Float(length) / Float(sections)
        
        var color: SIMD4<Float>?
        switch (section) {
        case 0:
            let blue = 0.5 * (Float(index + 1) / sectionWidth + 1.0)
            color = SIMD4<Float>(x: 0.0, y: 0.0, z: blue, w: 1.0)
        case 1, 2:
            let green = 0.5 * (Float(index + 1) / sectionWidth - 1.0)
            color = SIMD4<Float>(x: 0.0, y: green, z: 1.0, w: 1.0)
        case 3, 4:
            let red = 0.5 * (Float(index + 1) / sectionWidth - 3.0)
            let blue = 1.0 - 0.5 * (Float(index + 1) / sectionWidth - 3.0)
            color = SIMD4<Float>(x: red, y: 1.0, z: blue, w: 1.0)
        case 5, 6:
            let green = 1.0 - 0.5 * (Float(index + 1) / sectionWidth - 5.0)
            color = SIMD4<Float>(x: 1.0, y: green, z: 0.0, w: 1.0)
        case 7:
            let red = 1.0 - 0.5 * (Float(index + 1) / sectionWidth - 7.0)
            color = SIMD4<Float>(x: red, y: 0.0, z: 0.0, w: 1.0)
        default:
            color = nil
        }
        
        return color
    }
    
